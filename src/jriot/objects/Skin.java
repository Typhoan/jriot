package jriot.objects;

public class Skin
{
	private int id;
	private String name;
	private int num;

	/**
	 * Returns id.
	 *
	 * @return int id.
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * Sets id.
	 *
	 * @param id id.
	 */
	public void setId(int id)
	{
		this.id = id;
	}

	/**
	 * Returns name.
	 *
	 * @return String name.
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * Sets name.
	 *
	 * @param name name.
	 */
	public void setName(String name)
	{
		this.name = name;
	}

	/**
	 * Returns num.
	 *
	 * @return int num.
	 */
	public int getNum()
	{
		return num;
	}

	/**
	 * Sets num.
	 *
	 * @param num num.
	 */
	public void setNum(int num)
	{
		this.num = num;
	}
}