package jriot.objects;

public class Image
{
	private String full;
	private String group;
	private String sprite;
	private int h;
	private int w;
	private int x;
	private int y;
	
	/**
    * Returns the full of the image.
	 *
	 * @return String full of the image.
    */
	public String getFull()
	{
		return full;
	}
	
	/**
    * Sets the full of the image.
	 *
	 * @param full full of the image.
    */
	public void setFull(String full)
	{
		this.full =  full;
	}
	
	/**
    * Returns the group of the image.
	 *
	 * @return String group of the image.
    */
	public String getGroup()
	{
		return group;
	}
	
	/**
    * Sets the group of the image.
	 *
	 * @param group group of the image.
    */
	public void setGroup(String group)
	{
		this.group = group;
	}
	
	/**
    * Returns the sprite of the image.
	 *
	 * @return String sprite of the image.
    */
	public String getSprite()
	{
		return sprite;
	}
	
	/**
    * Sets the sprite of the image.
	 *
	 * @param sprite sprite of the image.
    */
	public void setSprite(String sprite)
	{
		this.sprite = sprite;
	}
	
	/**
    * Returns the h demension of the image.
	 *
	 * @return int h demension of the image.
    */
	public int getH()
	{
		return h;
	}
	
	/**
    * Sets the h demension of the image.
	 *
	 * @param h h demension of the image.
    */
	public void setH(int h)
	{
		this.h = h;
	}
	
	/**
    * Returns the w demension of the image.
	 *
	 * @return int w demension of the image.
    */
	public int getW()
	{
		return w;
	}
	
	/**
    * Sets the w demension of the image.
	 *
	 * @param w  w demension of the image.
    */
	public void setW(int w)
	{
		this.w = w;
	}
	
	/**
    * Returns the x demension of the image.
	 *
	 * @return int x demension of the image.
    */
	public int getX()
	{
		return x;
	}
	
	/**
    * Sets the x demension of the image.
	 *
	 * @param x x demension of the image.
    */
	public void setX(int x)
	{
		this.x = x;
	}
	
	/**
    * Returns the y demension of the image.
	 *
	 * @return int y demension of the image.
    */
	public int gety()
	{
		return y;
	}
	
	/**
    * Sets the y demension of the image.
	 *
	 * @param y y demension of the image.
    */
	public void setY(int y)
	{
		this.y = y;
	}
}