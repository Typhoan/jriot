package jriot.objects;

public class BlockItem
{
	private int count;
	private int id;

	/**
	 * Returns count.
	 *
	 * @return int count.
	 */
	public int getCount()
	{
		return count;
	}

	/**
	 * Sets count.
	 *
	 * @param count count.
	 */
	public void setCount(int count)
	{
		this.count = count;
	}

	/**
	 * Returns id.
	 *
	 * @return int id.
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * Sets id.
	 *
	 * @param id id.
	 */
	public void setId(int id)
	{
		this.id = id;
	}
}